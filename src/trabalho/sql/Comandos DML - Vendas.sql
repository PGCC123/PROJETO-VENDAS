-- CONSULTA LETRA A
SELECT VENDA.VDA_CODIGO AS "CODIGO", 
       VENDA.VDA_DATA   AS "DATA", 
       PESSOA.PES_NOME  AS "CLIENTE", 
       VENDA.VDA_TOTAL  AS "TOTAL",
       PESSOA.PES_UF    AS "UF" -- OBS: TIRAR ESSA COLUNA
FROM   SISTEMA.VENDA
INNER JOIN SISTEMA.CLIENTE
ON VENDA.CLI_CODIGO = CLIENTE.CLI_CODIGO
INNER JOIN SISTEMA.PESSOA
ON CLIENTE.PES_CODIGO = PESSOA.PES_CODIGO
WHERE VDA_DATA BETWEEN '01.01.2000' AND SYSDATE AND PES_UF = 'SP';

-- CONSULTA LETRA B
SELECT COMPRA.CRP_CODIGO          AS "CODIGO", 
       COMPRA.CRP_EMISSAO         AS "DATA EMISSÃO", 
       COMPRA.CRP_DTENTRADA       AS "DATA ENTRADA",
       USUARIO.USU_NOME           AS "USUÁRIO", 
       UPPER(PESSOA.PES_FANTASIA) AS "FORNECEDOR" --USUARIO.USU_ATIVO, PESSOA.PES_FISICA AS "PESSOA FÍSICA?"
FROM   SISTEMA.PESSOA
INNER JOIN SISTEMA.FORNECEDOR
ON PESSOA.PES_CODIGO = FORNECEDOR.PES_CODIGO
INNER JOIN SISTEMA.COMPRA
ON FORNECEDOR.FOR_CODIGO = COMPRA.FOR_CODIGO
INNER JOIN SISTEMA.USUARIO 
ON USUARIO.USU_CODIGO = COMPRA.USU_CODIGO
WHERE USUARIO.USU_NOME LIKE '%e%' AND USUARIO.USU_ATIVO = 'ATIVO' AND PESSOA.PES_FISICA = 'NÃO';

-- CONSULTA LETRA C
SELECT CLIENTE.CLI_CODIGO         AS "CODIGO", 
       PESSOA.PES_NOME            AS "NOME",
       UPPER(FORMAPAGTO.FPG_NOME) AS "PAGAMENTO", 
       VENDA.VDA_DATA             AS "DATA",
       VENDA.VDA_TOTAL            AS "TOTAL",
       PESSOA.PES_FISICA          AS "PESSOA FÍSICA?"      
FROM   SISTEMA.PESSOA
INNER JOIN SISTEMA.CLIENTE
ON PESSOA.PES_CODIGO = CLIENTE.PES_CODIGO
INNER JOIN SISTEMA.VENDA
ON CLIENTE.CLI_CODIGO = VENDA.CLI_CODIGO
INNER JOIN SISTEMA.VENDA_PAGTO
ON VENDA.VDA_CODIGO = VENDA_PAGTO.VDA_CODIGO
INNER JOIN SISTEMA.FORMAPAGTO
ON VENDA_PAGTO.FPG_CODIGO = FORMAPAGTO.FPG_CODIGO
WHERE PESSOA.PES_FISICA = 'SIM' AND FORMAPAGTO.FPG_NOME = 'Dinheiro' 
    AND VENDA.VDA_TOTAL BETWEEN 100.00 AND 2560.00 AND VENDA.VDA_DATA BETWEEN '01.01.2010' AND '31.12.2018';

-- CONSULTA LETRA D
SELECT CLIENTE.CLI_CODIGO      AS "CODIGO", 
       PESSOA.PES_NOME         AS "NOME", 
       COUNT(VENDA.VDA_CODIGO) AS "QUANTIDADE VENDA", 
       SUM(VENDA.VDA_TOTAL)    AS "TOTAL VENDAS"
FROM   SISTEMA.VENDA
INNER JOIN SISTEMA.CLIENTE
ON CLIENTE.CLI_CODIGO = VENDA.CLI_CODIGO
INNER JOIN SISTEMA.PESSOA
ON PESSOA.PES_CODIGO = CLIENTE.PES_CODIGO 
GROUP BY CLIENTE.CLI_CODIGO, PESSOA.PES_NOME
ORDER BY PES_NOME;

-- CONSULTA LETRA E
SELECT CP.PRO_CODIGO    AS "CODIGO", 
       PD.PRO_NOME      AS "PRODUTO", 
       CP.CPP_QTDE      AS "QUANTIDADE", 
       CP.CPP_PRECO     AS "PREÇO", 
       CP.CPP_DESCONTO  AS "DESCONTO", 
       CP.CPP_TOTAL     AS "TOTAL",
       CM.CRP_DTENTRADA AS "DATA DE ENTRADA", -- OBS: TIRA ESSA COLUNA
       PD.PRO_ATIVO     AS "STATUS"
FROM   SISTEMA.PRODUTO PD
INNER JOIN SISTEMA.COMPRA_PRODUTO CP
ON PD.PRO_CODIGO = CP.PRO_CODIGO
INNER JOIN SISTEMA.COMPRA CM
ON CM.CRP_CODIGO = CP.CRP_CODIGO
WHERE PD.PRO_CODIGO BETWEEN 43 AND 61 AND PD.PRO_ATIVO = 'INATIVO' 
    AND PD.PRO_NOME LIKE '%A%' AND CM.CRP_DTENTRADA IS NULL;
    

