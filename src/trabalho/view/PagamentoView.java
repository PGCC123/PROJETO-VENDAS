    private void btnGRAVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGRAVARActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Confirma Gravação desta Forma de pagamento ?",
                "Confirmação", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                PagamentoModel objpagamento = new PagamentoModel();
                objpagamento.setFPG_CODIGO(Integer.parseInt(edtFPG_CODIGO.getText()));
                objpagamento.setFPG_NOME(edtFPG_NOME.getText());
                objpagamento.setFPG_ATIVO(comboFPG_ATIVO.getSelectedItem().toString());

                PagamentoController pagamentocontroller = new PagamentoController();
                pagamentocontroller.gravar(getOperacao(), objpagamento);

                JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso");
                consultar();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro na Gravação \n" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnGRAVARActionPerformed

