    private void mostrar(PagamentoModel pagamento) {
        edtFPG_CODIGO.setText(String.valueOf(pagamento.getFPG_CODIGO()));
        edtFPG_NOME.setText(pagamento.getFPG_NOME());
        comboFPG_ATIVO.setSelectedItem(pagamento.getFPG_ATIVO());
    }

    private String filtroConsulta() {
        String condicao = "";
        if (!edtCONS_ID1.getText().trim().equals("")) {
            condicao += "(FPG_CODIGO >= " + edtCONS_ID1.getText() + ")";
        }
        if (!edtCONS_ID2.getText().trim().equals("")) {
            if (!condicao.isEmpty()) {
                condicao += " AND ";
            }
            condicao += "(FPG_CODIGO <= " + edtCONS_ID2.getText() + ")";
        }
        if (!edtCONS_NOME.getText().trim().equals("")) {
            if (!condicao.isEmpty()) {
                condicao += " AND ";
            }
            condicao += "(FPG_NOME LIKE ('%" + edtCONS_NOME.getText() + "%'))";
        }

        return condicao;
    }

    private void consultar() {
        try {
            String condicao = filtroConsulta();
            PagamentoController pagamentocontroller = new PagamentoController();
            lista = null;
            lista = pagamentocontroller.consultar(condicao);
            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Não Existem Formas de Pagamentos Cadastrados !");
            } else {
                tabela = new PagamentoTableModel(lista, colunas);
                tblConsulta.setModel(tabela);
                tblConsulta.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na Consulta da Forma de pagamento \n" + ex.getMessage());
        }
    }

